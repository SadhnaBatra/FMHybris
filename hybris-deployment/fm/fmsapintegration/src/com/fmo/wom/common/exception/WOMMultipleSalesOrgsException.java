package com.fmo.wom.common.exception;

public class WOMMultipleSalesOrgsException extends WOMValidationException {

	public WOMMultipleSalesOrgsException() { }

	public WOMMultipleSalesOrgsException(String message) {
		super(message);
	}

	public WOMMultipleSalesOrgsException(Throwable cause) {
		super(cause);
	}

	public WOMMultipleSalesOrgsException(String message, Throwable cause) {
		super(message, cause);
	}

	public WOMMultipleSalesOrgsException(boolean doSelfLog) {
		super(doSelfLog);
	}

	public WOMMultipleSalesOrgsException(String message, boolean doSelfLog) {
		super(message, doSelfLog);
	}

	public WOMMultipleSalesOrgsException(String message, Throwable cause,
			boolean doSelfLog) {
		super(message, cause, doSelfLog);
	}

	public WOMMultipleSalesOrgsException(Throwable cause, boolean doSelfLog) {
		super(cause, doSelfLog);
	}
	
	
}
